// JavaScript Document
function fn_spread(id){
    var getID = document.getElementById(id);
    getID.style.display=(getID.style.display=='block') ? 'none' : 'block';
	var img1 = document.getElementById("img1");
	getID.style.display=(getID.style.display=='block') ? img1.src="resources/images/board/button.png" : img1.src="resources/images/board/button2.png";
}
