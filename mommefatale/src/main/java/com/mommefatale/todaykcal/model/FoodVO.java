package com.mommefatale.todaykcal.model;

public class FoodVO {

	private int food_no;
	private String food_category;
	private String food_name;
	private int food_gram;
	private int food_kcal;

	public int getFood_no() {
		return food_no;
	}

	public void setFood_no(int food_no) {
		this.food_no = food_no;
	}

	public String getFood_category() {
		return food_category;
	}

	public void setFood_category(String food_category) {
		this.food_category = food_category;
	}

	public String getFood_name() {
		return food_name;
	}

	public void setFood_name(String food_name) {
		this.food_name = food_name;
	}

	public int getFood_gram() {
		return food_gram;
	}

	public void setFood_gram(int food_gram) {
		this.food_gram = food_gram;
	}

	public int getFood_kcal() {
		return food_kcal;
	}

	public void setFood_kcal(int food_kcal) {
		this.food_kcal = food_kcal;
	}

}
